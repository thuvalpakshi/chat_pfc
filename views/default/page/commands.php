<h3>Chat Commands :</h3><p>Here is a list of commands that can be used when chatting.</p>
<div style="width:340px; padding: 0 20px 0 0; float:left;">
<p><strong>ban</strong> : Bans an user and kicks it from the room</p>
<blockquote>
<p>/ban "{nickname}" {reason}</p>
</blockquote>
<p><strong>banlist</strong> : This command lists the banished users on the given channel</p>
<blockquote>
<p>/banlist</p>
</blockquote>
<p><strong>clear</strong> : Clears the room's messages (but not the chat file logs)</p>
<blockquote>
<p>/clear</p>
</blockquote>
<p><strong>connect</strong> : Connects to the chat</p>
<blockquote>
<p>/connect {nickname}</p>
</blockquote>
<p><strong>debug</strong> : Shows debug infos</p>
<blockquote>
<p>/debug [userconfig|globalconfig|phpserver]</p>
</blockquote>
<p><strong>help</strong> : Shows the command list</p>
<blockquote>
<p>/help</p>
</blockquote>
<p><strong>identify</strong> : This command will identify the user admin rights</p>
<blockquote>
<p>/identify {password}</p>
</blockquote>
<p><strong>init</strong> : Initialize the chat</p>
<blockquote>
<p>/init</p>
</blockquote>
<p><strong>invite</strong> : Invites other users into a channel</p>
<blockquote>
<p>/invite {nickname to invite} [ {target channel} ]</p>
</blockquote>
<p><strong>join</strong> : Creates a new channel</p>
<blockquote>
<p>/join {channelname}</p>
</blockquote>
<p><strong>kick</strong> : Kicks the user from a channel</p>
<blockquote>
<p>/kick "{nickname}" [ {reason} ]</p>
</blockquote>
<p><strong>leave</strong></p>
<blockquote>
<p>/leave [ch|pv [[{channel|nickname}] {reason}]]</p>
</blockquote>



</div><div style="width:340px; padding: 0 0 0 20px; float:left;">




<p><strong>me</strong> : Writes a notable message</p>
<blockquote>
<p>/me {message}</p>
</blockquote>
<p><strong>nick</strong> : Change the nick (rename it) and notify all the joined channels/privmsg</p>
<blockquote>
<p>/nick {newnickname}</p>
</blockquote>
<p><strong>op</strong> : Makes the user admin. Just changes the &ldquo;isadmin&rdquo; meta flag</p>
<blockquote>
<p>/op {nickname}</p>
</blockquote>
<p><strong>deop</strong> : Makes the user no-admin. Just change the &ldquo;isadmin&rdquo; meta flag</p>
<blockquote>
<p>/deop {nickname}</p>
</blockquote>
<p><strong>privmsg</strong> : Send Private message to an user</p>
<blockquote>
<p>/privmsg {nickname}</p>
</blockquote>
<p><strong>quit</strong> : Logouts the user from the chat</p>
<blockquote>
<p>/quit</p>
</blockquote>
<p><strong>redirect</strong> : Opens the url in the browser</p>
<blockquote>
<p>/redirect url</p>
</blockquote>
<p><strong>rehash</strong> : This command deletes the cached configuration</p>
<blockquote>
<p>/rehash</p>
</blockquote>
<p><strong>send</strong> : Sends a text to the room</p>
<blockquote>
<p>/send {text to send}</p>
</blockquote>
<p><strong>unban</strong> : Will unban the user</p>
<blockquote>
<p>/unban {nickname}</p>
</blockquote>
<p><strong>unban all</strong> : will unban all the users on this channel</p>
<blockquote>
<p>/unban all</p>
</blockquote>
<p><strong>who</strong> : shows whos information</p>
<blockquote>
<p>/who channel</p>
</blockquote>
<p><strong>whois</strong> : Shows the user's details</p>
<blockquote>
<p>/whois {nickname}<br /><br /></p>
</blockquote>
</div>